#include <unistd.h>

int	main(int argc, char **argv)
{
	int	c1;
	int	c2;

	c1 = 1;
	c2 = 0;
	if (argc > 1)
	{
		while (c1 < argc)
		{
			c2 = 0;
			while (argv[c1][c2])
			{
				write(1, &argv[c1][c2], 1);
				c2++;
			}
			write(1, "\n", 1);
			c1++;
		}
	}
}
